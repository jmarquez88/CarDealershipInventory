    private void jComboBoxMakeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBoxMakeActionPerformed
        // TODO add your handling code here:
        int index = jComboBoxMake.getSelectedIndex();
        
        jComboBoxModel.removeAllItems();
        
        if(index == 0)
        {
            for(int i = 0; i < toyotaModels.length; i++)
            {
                jComboBoxModel.addItem(toyotaModels[i]);
            }
        }
        else if(index == 1)
        {
            for(int i = 0; i < lexusModels.length; i++)
            {
                jComboBoxModel.addItem(lexusModels[i]);
            }
        }
        else if(index == 2)
        {
            for(int i = 0; i < infinitModels.length; i++)
            {
                jComboBoxModel.addItem(infinitModels[i]);
            }
        }
        else if(index == 3)
        {
            for(int i = 0; i < acuraModels.length; i++)
            {
                jComboBoxModel.addItem(acuraModels[i]);
            }
        }
        else if(index == 4)
        {
            for(int i = 0; i < hyundaiModels.length; i++)
            {
                jComboBoxModel.addItem(hyundaiModels[i]);
            }
        }
        else if(index == 5)
        {
            for(int i = 0; i < nissanModels.length; i++)
            {
                jComboBoxModel.addItem(nissanModels[i]);
            }
        }
    }//GEN-LAST:event_jComboBoxMakeActionPerformed

