    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add the addition of a car, it is extremely recommended that you validate your information
                // TODO add your handling code here:
        InventoryFileReader iReader = new InventoryFileReader();
        ArrayList<Car> cars = iReader.getAllCars();
        
        String make = jComboBoxMake.getSelectedItem() + "";
        String model = jComboBoxModel.getSelectedItem() + "";
        int price = Integer.parseInt(jTextFieldPrice.getText());
        int year = Integer.parseInt(jComboBoxYear.getSelectedItem() + "");
        String color = jTextFieldCapacity.getText() + "";
        CarType carType = CarType.valueOf(jComboBoxCarType.getSelectedItem() + "");
        int miles = 100;
        FuelType fuelType = FuelType.valueOf(jComboBoxFuelType.getSelectedItem() + "");
        int noOfCylinders = Integer.parseInt(jcomboBoxNoOfCylinders.getSelectedItem() + "");
        int capacity = Integer.parseInt(jTextFieldCapacity.getText());
        int horsePower = Integer.parseInt(jTextFieldHorsePower.getText());
        int mpg = Integer.parseInt(jTextFieldMPG.getText());
        String color1 = jTextFieldInteriorColor1.getText();
        String color2 = jTextFieldInteriorColor2.getText();
        boolean sunRoof = false, moonRoof = false, panoramicRoof = false, spareTire= false, trunkCarpet = false, jack = false, firstAidkit = false;
        
        if(jCheckBoxSunRoof.isSelected())
            sunRoof = true;
        if(jCheckBoxMoonRoof.isSelected())
            moonRoof = true;
        if(jCheckBoxPanoramicRoof.isSelected())
            panoramicRoof = true;
        if(jCheckBoxSpareTire.isSelected())
            spareTire = true;
        if(jCheckBoxTrunkCarpet.isSelected())
            trunkCarpet = true;
        if(jCheckBoxJack.isSelected())
            jack = true;
        if(jCheckBoxFirstAidKit.isSelected())
            firstAidkit = true;
                
        
        Car car = new Car(make, year, model, price, color, miles, carType,new Engine(fuelType, noOfCylinders, capacity, horsePower, mpg),
                            new Interior(color1, color2, sunRoof, moonRoof, panoramicRoof), new Trunk(spareTire, firstAidkit, trunkCarpet, jack));
        
        iReader.addCar(cars, car);
        
        JOptionPane.showMessageDialog(this, "Added Vehicle!");

        this.setVisible(false);
        MainForm mForm;
    try {
        mForm = new MainForm();
        mForm.setVisible(false);
    } catch (IOException ex) {
        Logger.getLogger(ViewCarForm.class.getName()).log(Level.SEVERE, null, ex);
    }
    }//GEN-LAST:event_jButton2ActionPerformed

